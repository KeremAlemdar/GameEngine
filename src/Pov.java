public class Pov {
    double x,y,z,angle;

    public Pov(double x, double y, double z, double angle) {
        this.x = x;
        this.y = y;
        this.z= z;
        this.angle = angle;
    }
    public void setX(int x) {
        this.x = x;
    }
    public void setY(int y) {
        this.y = y;
    }
    public void setZ(int z) {
        this.z = z;
    }
    public void setAngle(int angle) {
        this.angle = angle;
    }
    
}
